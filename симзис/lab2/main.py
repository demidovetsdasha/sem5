alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'

def encrypt(text, key):

    if len(text) != len(key):
        print("Ключ должен быть одной длины с сообщением")

    text = text.upper()
    key = key.upper()
    encrypted_text = []

    for i in range(0, len(text)):
        if text[i] in alphabet:
            row_num = alphabet.index(key[i])
            col_num = alphabet.index(text[i])
            encrypted_text.append(table[row_num][col_num])
        else:
            encrypted_text.append(text[i])  # не изменяем символы, не входящие в алфавит

    return ''.join(encrypted_text)


def decrypt(encrypted_text, key):

    if len(encrypted_text) != len(key):
        print("Ключ должен быть одной длины с сообщением")

    encrypted_text = encrypted_text.upper()
    key = key.upper()
    original_text = []

    for i in range(0, len(encrypted_text)):
        if encrypted_text[i] in alphabet:
            row_num = alphabet.index(key[i])
            col_num = alphabet.index(encrypted_text[i]) - row_num

            if col_num < 0:
                col_num = col_num + len(alphabet)

            original_text.append(alphabet[col_num])
        else:
            original_text.append(encrypted_text[i]) # не изменяем символы, не входящие в алфавит

    return ''.join(original_text)

